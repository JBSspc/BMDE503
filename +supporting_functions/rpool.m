	  1.82 6.98 12.7 23.2 42.2 76.8 140 255  ...
	  2.00 7.15 13.0 23.7 43.2 78.7 143 261  ...
	  2.21 7.32 13.3 43.3 44.2 80.6 147 267  ...
	  2.43 7.50 13.7 24.9 45.3 82.5 150 274  ...
	  2.67 7.68 14.0 25.5 46.4 84.5 154 280  ...
	  3.01 7.87 14.3 26.1 47.5 86.6 158 287  ...
	  3.32 8.06 14.7 26.7 48.7 88.7 162 294  ...
