function y=rpool
% RPOOL Returns resistor base values from the 1% line.
%
%       R  = RPOOL returns resistor base values from the 1% resistor
%       line. Values are in the range of [1, 10] and may be scaled
%       by 10^n, where n: [1, 6].

% rpool holds the resistor pool values from the 1%-tolerance line.
% The resistor values have been entered the same way that they appear in
% the ElectroSonic catalog.  Allowable resistor values are formed by
% multiplying pool entries by powers of 10.

rpool = [ 1.00 6.04 11.0 20.0 36.5 66.5 121 221  ...
	  1.10 6.19 11.3 20.5 37.4 68.1 124 226  ...
