	  5.76 10.5 19.1 34.8 63.4 115  210 383  ...
	  5.90 10.7 19.6 35.7 64.9 118  215 392 ]';


% For the sake of organization, the resistor pool will be reduced
% to base values in the range of [1.0 .. 10.0].

n = fix(log10(rpool));         % Find magnitude of each resistance

rpool = rpool ./ 10 .^ n;      % Normalize each value

rpool = sort(rpool);           % Sort pool in increasing order

% The normalization usually produces repeating resistance values.
% Eliminate duplicate values.

d = diff(rpool);           % Make length(d) == length(rpool) so that
d = [d; 1];                % last value of rpool is also picked out.
i = find(d > 1.0e-05);     % Indices of different resistance values.
rpool = rpool(i);          % Extract the resistance values: [1.0 .. 10.0)

y = [rpool; 10*rpool(1)];  % Now include 10.0 in the resistance pool.
